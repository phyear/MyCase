package com.kc.model;

public class RoleUrl {
    private Long id;

    private Long roleid;

    private Long urlid;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getRoleid() {
        return roleid;
    }

    public void setRoleid(Long roleid) {
        this.roleid = roleid;
    }

    public Long getUrlid() {
        return urlid;
    }

    public void setUrlid(Long urlid) {
        this.urlid = urlid;
    }
}